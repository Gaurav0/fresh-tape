#!/usr/bin/env node

'use strict';

/**
 * Reads test-baseline/tap-tests-output.txt and tap-compat-output.txt and
 * rewrites test-baseline/inventory.md with per-file status tables.
 */

var fs = require('fs');
var path = require('path');
var glob = require('globv7');

var root = path.join(__dirname, '..');
var outDir = path.join(root, 'test-baseline');
var tapLog = path.join(outDir, 'tap-tests-output.txt');
var compatLog = path.join(outDir, 'tap-compat-output.txt');
var inventoryPath = path.join(outDir, 'inventory.md');

function readUtf8(p) {
    try {
        return fs.readFileSync(p, 'utf8');
    } catch (e) {
        return '';
    }
}

/** Top-level tap file lines: ok 18 - test/error.js # time=... */
function parseTapFileStatus(text) {
    var fileStatus = {};
    var re = /^(ok|not ok) (\d+) - (test\/.+?)(?=\s+#|\s*$)/gm;
    var m;
    while ((m = re.exec(text)) !== null) {
        var idx = m.index;
        var lineEnd = text.indexOf('\n', idx);
        var line = lineEnd === -1 ? text.slice(idx) : text.slice(idx, lineEnd);
        var file = m[3].trim();
        var st;
        if (m[1] === 'not ok') {
            st = 'fail';
        } else if ((/# SKIP\b/).test(line)) {
            st = 'skip';
        } else {
            st = 'pass';
        }
        fileStatus[file] = st;
    }
    return fileStatus;
}

var tapText = readUtf8(tapLog);
var compatText = readUtf8(compatLog);
var fileStatus = Object.assign({}, parseTapFileStatus(tapText), parseTapFileStatus(compatText));

var mainFiles = glob.sync('test/*.js', { cwd: root }).sort();
var compatFiles = glob.sync('test/compat/*.js', { cwd: root }).sort();

var lines = [];
lines.push('# Baseline test inventory');
lines.push('');
lines.push('Generated by `scripts/update-baseline-inventory.js` after `npm run test:baseline`.');
lines.push('');
lines.push('`tests-only` matches `test/*.js`; compat run matches `test/compat/*.js` (same as `npm run tests-compat`).');
lines.push('');
lines.push('## Main suite (`test/*.js`)');
lines.push('');
lines.push('| Test file | Last tap result |');
lines.push('|-----------|-----------------|');

mainFiles.forEach(function (f) {
    var s = fileStatus[f];
    if (!s) {
        s = '— (not matched in tap log; re-run baseline)';
    }
    lines.push('| `' + f + '` | **' + s + '** |');
});

lines.push('');
lines.push('## Compat suite (`test/compat/*.js`)');
lines.push('');
lines.push('| Test file | Last tap result |');
lines.push('|-----------|-----------------|');

compatFiles.forEach(function (f) {
    var s = fileStatus[f];
    if (!s) {
        s = '— (not matched in tap log; re-run baseline)';
    }
    lines.push('| `' + f + '` | **' + s + '** |');
});

lines.push('');
lines.push('## Aggregate lines from tap');
var aggMain = tapText.match(/^# (failed \d+ of \d+ tests|tests? \d+)$/m);
lines.push('Main: ' + (aggMain ? '`' + aggMain[1] + '`' : '_(run baseline to populate)_'));
var aggCompat = compatText.match(/^# (failed \d+ of \d+ tests|tests? \d+)$/m);
lines.push('Compat: ' + (aggCompat ? '`' + aggCompat[1] + '`' : '_(run baseline to populate)_'));
lines.push('');

fs.writeFileSync(inventoryPath, lines.join('\n'), 'utf8');
process.stdout.write('Wrote ' + inventoryPath + '\n');

#!/usr/bin/env node

'use strict';

/**
 * Reads test-baseline/tap-tests-output.txt (tap top-level file results) and
 * rewrites test-baseline/inventory.md with a per-file status table.
 */

var fs = require('fs');
var path = require('path');
var glob = require('glob');

var root = path.join(__dirname, '..');
var outDir = path.join(root, 'test-baseline');
var tapLog = path.join(outDir, 'tap-tests-output.txt');
var inventoryPath = path.join(outDir, 'inventory.md');

var tapText = '';
try {
    tapText = fs.readFileSync(tapLog, 'utf8');
} catch (e) {
    tapText = '';
}

/** Top-level tap file lines: ok 18 - test/error.js # time=... (do not stop at # in # time — use lookahead) */
var fileStatus = {};
var re = /^(ok|not ok) (\d+) - (test\/.+?)(?=\s+#|\s*$)/gm;
var m;
while ((m = re.exec(tapText)) !== null) {
    var idx = m.index;
    var lineEnd = tapText.indexOf('\n', idx);
    var line = lineEnd === -1 ? tapText.slice(idx) : tapText.slice(idx, lineEnd);
    var file = m[3].trim();
    var st;
    if (m[1] === 'not ok') {
        st = 'fail';
    } else if ((/# SKIP\b/).test(line)) {
        st = 'skip';
    } else {
        st = 'pass';
    }
    fileStatus[file] = st;
}

var files = glob.sync('test/*.js', { cwd: root }).sort();

var lines = [];
lines.push('# Baseline test inventory');
lines.push('');
lines.push('Generated by `scripts/update-baseline-inventory.js` after `npm run test:baseline`.');
lines.push('');
lines.push('`tests-only` runs **only** top-level `test/*.js` (same as `package.json` `tests-only`).');
lines.push('');
lines.push('| Test file | Last tap result |');
lines.push('|-----------|-----------------|');

files.forEach(function (f) {
    var s = fileStatus[f];
    if (!s) {
        s = '— (not matched in tap log; re-run baseline)';
    }
    lines.push('| `' + f + '` | **' + s + '** |');
});

lines.push('');
lines.push('## Aggregate line from tap');
var agg = tapText.match(/^# (failed \d+ of \d+ tests|tests? \d+)$/m);
lines.push(agg ? '`' + agg[1] + '`' : '_(run `npm run test:baseline` to populate)_');
lines.push('');

fs.writeFileSync(inventoryPath, lines.join('\n'), 'utf8');
process.stdout.write('Wrote ' + inventoryPath + '\n');
